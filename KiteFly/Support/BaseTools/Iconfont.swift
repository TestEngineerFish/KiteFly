    case select         = "&"
    case more           = "a"
    case video          = "v"
    case close          = "c"
    /// 右箭头
    case arrow          = "\u{e7f0}"
    /// 单实线向下箭头
    case arrowDown      = "\u{e7f3}"
    /// 单实线向上箭头
    case arrowUp        = "\u{e7ee}"
}
